public class Errors {
    public static void main(String[] args) {
        //Errors: No matter how we smart are.Errors are constant but by practice we can understand them easily
        //and handle it.

        //There are three types of error:
        //1)Syntax error 2)logical error 3)Runtime error(also called Exception).

        //1) Syntax error:
        //int a = 5 -----> Throws syntax error.
        int a = 5;
        System.out.println(a);

        //Key point:Syntax and logical error are encountered by developer and runtime error (exception) are
        //are encountered by users.
    }
}

