public class for_loop {
    public static void main(String[] args) {
        for (int i=1; i<=10; i++){
            System.out.println(i);
        }

        //Quiz
        for (int a=0; a<=10; a++){
            System.out.println((2*a)+1+" (Odd number)");
        }

        for (int b=7; b>=0; b--){
            System.out.println(b);
        }
    }
}
