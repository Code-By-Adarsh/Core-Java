public class do_while_loop {
    public static void main(String[] args) {
        int i = 0;
        do{
            System.out.println(i);
            i++;
        }while(i<5);

        System.out.println("First 10 natural numbers using do-while loop.");
        int a = 1;
        do{
            System.out.println(a);
            a++;
        }while(a<11);
    }
}
